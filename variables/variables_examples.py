# What is a variable?
# A variable is a named storage location in memory used to hold a value that a program can use or modify later.

# Why called a variable?
# Because its value can change or vary during program execution, it’s not fixed.

# Try to set a variable with a number value
# Try to set a variable with a decimal value
# Try to set a variable with a string value

